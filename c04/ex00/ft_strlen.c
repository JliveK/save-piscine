int ft_strlen(char *str)
{
    int x;
    x = 0;

    while (str[x] != '\0')
    {
        x++;
    }
    return (x);
}

# include <stdio.h>
int main ()
{
	printf("%d\n",ft_strlen("12345"));
	printf("%d",ft_strlen(""));
}
